package POJO;

import java.util.Date;

public class contagemPOJO {
    private Integer ID;
    private String IP;
    private Date DataCadastro;
    public Integer getID() {
        return ID;
    }

    public void setID(Integer ID) {
        this.ID = ID;
    }

    public String getIP() {
        return IP;
    }

    public void setIP(String IP) {
        this.IP = IP;
    }

   

    public Date getDataCadastro() {
        return DataCadastro;
    }


    public void setDataCadastro(Date DataEvento) {
        this.DataCadastro = DataEvento;
    }


  
}
