package POJO;

/**
 *
 * @author PC
 */
public class fotosPaginasPOJO {
    private Integer ID;
    private Integer Id_Paginas;
    private String titulo_Paginas;
    private String Titulo;
    private String Descricao;
    private String Src;

    /**
     * @return the ID
     */
    public Integer getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(Integer ID) {
        this.ID = ID;
    }

    /**
     * @return the Id_Categoria
     */
    public Integer getId_Paginas() {
        return Id_Paginas;
    }

    /**
     * @param Id_Categoria the Id_Categoria to set
     */
    public void setId_Paginas(Integer Id_Categoria) {
        this.Id_Paginas = Id_Categoria;
    }

    /**
     * @return the Titulo
     */
    public String getTitulo() {
        return Titulo;
    }

    /**
     * @param Titulo the Titulo to set
     */
    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    /**
     * @return the Descricao
     */
    public String getDescricao() {
        return Descricao;
    }

    /**
     * @param Descricao the Descricao to set
     */
    public void setDescricao(String Descricao) {
        this.Descricao = Descricao;
    }

    /**
     * @return the Src
     */
    public String getSrc() {
        return Src;
    }

    /**
     * @param Src the Src to set
     */
    public void setSrc(String Src) {
        this.Src = Src;
    }

    /**
     * @return the titulo_Categoria
     */
    public String getTitulo_Paginas() {
        return titulo_Paginas;
    }

    /**
     * @param titulo_Categoria the titulo_Categoria to set
     */
    public void setTitulo_Paginas(String titulo_Categoria) {
        this.titulo_Paginas = titulo_Categoria;
    }
}
